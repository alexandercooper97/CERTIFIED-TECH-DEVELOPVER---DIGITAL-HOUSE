/*
Igualdad, if y else
¡Juguemos con condiciones!

En este ejercicio nos viene dada una variable llamada lenguaje, 
que tiene asignado un string. Nuestro trabajo es construir 
un condicional if/else para que, si la variable contiene "javascript", 
imprima por consola el texto "Estoy aprendiendo". 
De lo contrario, que imprima"Lo aprenderé más adelante".
*/

let lenguaje = "javascript";

if (lenguaje == "javascript"){
    console.log("Estoy aprendiendo");
}
else{
    console.log("Lo aprenderé más adelante");
}